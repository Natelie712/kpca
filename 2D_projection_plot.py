plt.axvline(0, color="gray", linestyle="--", alpha=0.5)

plt.xlabel("Projection onto global steering direction")
plt.ylabel("Projection onto dominant orthogonal direction")
plt.title("2D projection of point-wise kPCA steering displacements")
plt.tight_layout()
plt.show()

