import re

def extract_answer_letter(resp):
    """
    Try to extract answer letter from response text.
    Returns 'A', 'B', 'C', 'D' or None.
    """
    if resp is None:
        return None
    m = re.search(r"\(([A-D])\)", resp)
    if m:
        return m.group(1)
    return None

