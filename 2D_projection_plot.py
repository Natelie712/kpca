def extract_layer_activations(dataset, layer_id):
    """
    Returns:
        X: (N, D) numpy array
        y: (N,) labels in {-1, +1}
    """
    layer_key = f"model.layers.{layer_id}.resid"

    X = np.stack([np.array(x, dtype=np.float32) for x in dataset[layer_key]])
    y = np.array(dataset["label"], dtype=np.int64)

    return X, y

X, y = extract_layer_activations(ds, layer_id=10)

print("X shape:", X.shape)
print("y shape:", y.shape)
print("labels:", np.unique(y))

k = 50
epsilon = 1e-2

kpca = KernelPCA(
    n_components=k,
    kernel="poly",
    degree=3,
    gamma=1.0 / X.shape[1],
    coef0=1.0,
    fit_inverse_transform=True
)

Z = kpca.fit_transform(X)
print("Z shape:", Z.shape)

Z_pos = Z[y == 1]
Z_neg = Z[y == -1]

mu_pos = Z_pos.mean(axis=0)
mu_neg = Z_neg.mean(axis=0)

mu_pos_x = X[y == 1].mean(axis=0)
mu_neg_x = X[y == -1].mean(axis=0)

v_global = mu_pos_x - mu_neg_x
v_global = v_global / (np.linalg.norm(v_global) + 1e-8)

u_primes = []

for i in range(len(X)):
    z_i = Z[i]

    if y[i] == 1:
        u_i = mu_neg - z_i
    else:
        u_i = mu_pos - z_i

    norm = np.linalg.norm(u_i)
    if norm < 1e-8:
        continue
    u_i = u_i / norm

    z_i_prime = z_i + epsilon * u_i
    x_i_prime = kpca.inverse_transform(z_i_prime.reshape(1, -1))[0]

    u_i_prime = x_i_prime - X[i]
    norm_u = np.linalg.norm(u_i_prime)
    if norm_u < 1e-8:
        continue
    u_i_prime = u_i_prime / norm_u

    u_primes.append(u_i_prime)

u_primes = np.stack(u_primes)

# e1: global steering direction
e1 = v_global / (np.linalg.norm(v_global) + 1e-8)

# remove e1 component from u_i'
residuals = u_primes - (u_primes @ e1)[:, None] * e1[None, :]

# PCA on residuals to get dominant spread direction
U, S, Vt = np.linalg.svd(residuals, full_matrices=False)
e2 = Vt[0]

proj_1 = u_primes @ e1
proj_2 = u_primes @ e2

plt.figure(figsize=(6,6))
plt.scatter(proj_1, proj_2, s=5, alpha=0.4)
